from datetime import datetime
def get_days_from_today(date: str) -> int | None:
    """
    Розраховує кількість днів між заданою датою і поточною датою.
    :param date: Рядок, що представляє дату у форматі 'РРРР-ММ-ДД' (наприклад, '2020-10-09').
    :return: Ціле число, яке вказує на кількість днів від заданої дати до поточної.
             Якщо задана дата пізніша за поточну, результат буде від'ємним.
             Повертає None у разі неправильного формату або типу вхідних даних.
    """
    try:
        # Перетворюємо рядок дати у об'єкт date (ігноруючи час)
        given_date = datetime.strptime(date, "%Y-%m-%d").date()
        
        # Отримуємо поточну дату без урахування часу (години, хвилини, секунди)
        today_date = datetime.today().date()
        
        # Розраховуємо різницю між поточною та заданою датою
        # (today_date - given_date) дасть від'ємне значення, якщо given_date у майбутньому
        delta = today_date - given_date
        
        # Повертаємо різницю в днях як ціле число
        return delta.days
    except ValueError:
        # Обробка винятків при некоректному форматі або неіснуючій даті (наприклад, 31 лютого)
        print(f"Помилка: Неправильний формат дати або значення '{date}'. Очікується формат 'РРРР-ММ-ДД'.")
        return None
    except TypeError:
        # Обробка винятків, якщо передано не рядок
        print(f"Помилка: Параметр має бути рядком (str). Отримано {type(date)}.")
        return None
print(get_days_from_today("2025-12-31"))